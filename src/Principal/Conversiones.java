package Principal;


public class Conversiones {
	
	
	
	public float pesoDolarEEUU(int input) {
		return input * (float) 0.00505;
	}
	
	public float pesoYen(int input) {
		return input * (float) 1.45372;
	}
	
	public float pesoEuro(int input) {
		return input * (float) 0.00476;
	}
	
	public float pesoLibra(int input) {
		return input * (float) 237.582;
	}
	
	public float pesoWonSul(int input) {
		return input * (float) 0.15227;
	}

}
